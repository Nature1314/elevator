package com.fdmgroup;

public class People {
	
	private int floorEnterNum;
	private int floorExitNum;
	 
	public People(int floorEnterNum, int floorExitNum) {
		this.floorEnterNum = floorEnterNum;
		this.floorExitNum = floorExitNum;
	}

	public int getFloorEnterNum() {
		return floorEnterNum;
	}

	public void setFloorEnterNum(int floorEnterNum) {
		this.floorEnterNum = floorEnterNum;
	}

	public int getFloorExitNum() {
		return floorExitNum;
	}

	public void setFloorExitNum(int floorExitNum) {
		this.floorExitNum = floorExitNum;
	}
	
 
}
